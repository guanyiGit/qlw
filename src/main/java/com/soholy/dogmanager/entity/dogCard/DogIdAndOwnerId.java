package com.soholy.dogmanager.entity.dogCard;

public class DogIdAndOwnerId {
    private Long dogId;
    private Long dogOwnerId;
    private Integer isRepeat;//判断是否重复0未重复 1重复

    public Long getDogId() {
        return dogId;
    }

    public void setDogId(Long dogId) {
        this.dogId = dogId;
    }

    public Long getDogOwnerId() {
        return dogOwnerId;
    }

    public void setDogOwnerId(Long dogOwnerId) {
        this.dogOwnerId = dogOwnerId;
    }

    public Integer getIsRepeat() {
        return isRepeat;
    }

    public void setIsRepeat(Integer isRepeat) {
        this.isRepeat = isRepeat;
    }

    @Override
    public String toString() {
        return "DogIdAndOwnerId{" +
                "dogId=" + dogId +
                ", dogOwnerId=" + dogOwnerId +
                ", isRepeat=" + isRepeat +
                '}';
    }
}
