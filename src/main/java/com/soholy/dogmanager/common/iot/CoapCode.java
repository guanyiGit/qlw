package com.soholy.dogmanager.common.iot;

/**
 * 
 * @ClassName CoapCode
 * @Description (coap协议的请求code)
 * @author guanY
 * @Date 2018年8月30日 下午8:17:09
 * @version 1.0.0
 */
public enum CoapCode {

    GPS_UP_UP((byte) 0x01),
    WARN_UP_UP((byte) 0x02),
    SET_INTERVAL_TIME_DOWN((byte) 0x03),
    SET_INTERVAL_TIME_UP((byte) 0x04),
    SIMPLE_UP_UP((byte) 0x05),
    SET_WORK_PATTERN_DOWN((byte) 0x06),
    SET_WORK_PATTERN_UP((byte) 0x07),
    STARTING_UP((byte) 0x08),
    STARTING_DOWN((byte) 0x09),
    WIFI_UP_UP((byte) 0x0A);

    private byte code;

    private CoapCode(byte code) {
        this.code = code;
    }

    public byte getCode() {
        return code;
    }

    public void setCode(byte code) {
        this.code = code;
    }

}
